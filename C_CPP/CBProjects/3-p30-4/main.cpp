#include <iostream>

using namespace std;

int main()
{
    int $;
    cout << " 請輸入商品價格：";
    cin >> $;
    cout << " 找回 " << 1000-$ << " 元" << endl;
    cout << " 找回 500: " << (1000-$)/500 << " 張" << " 100: " << (1000-$)%500/100 << " 張"
        << " 50: " << (1000-$)%100/50 << " 個" << " 10: " << (1000-$)%50/10 << " 個" << " 5: " << (1000-$)%10/5 << " 個" << "  1: " << (1000-$)%5 << " 個" << endl;
    system("pause");
    return 0;
}
