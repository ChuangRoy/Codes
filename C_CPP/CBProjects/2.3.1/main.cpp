#include <iostream>

using namespace std;

int main()
{
    int a = 5,b = 10;
    cout << a << " 與 " << b << " 的和 = " << a + b << endl;
    cout << a << " 與 " << b << " 的差 = " << a - b << endl;
    cout << a << " 與 " << b << " 的積 = " << a * b << endl;
    cout << a << " 與 " << b << " 的商 = " << (float) a / b << endl;
    return 0;
}
